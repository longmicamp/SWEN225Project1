public class Room {

    private String Weapon;
    private String Room;



    public Room(String room) {
        super();
        Room = room;
    }

    public String getRoom() {
        return Room;
    }

    public void setRoom(String room) {
        Room = room;
    }

    public String getWeapon() {
        return Weapon;
    }

    public void setWeapon(String weapon) {
        Weapon = weapon;
    }
}




